    private void btnPlaceListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlaceListActionPerformed
        try {
            String[] places = this.client.listPlaces();
            DefaultListModel listModel = new DefaultListModel();
            for (String place : places) {
                String[] info = place.split("\\|");
                String item = info[0] + "(lat:" + info[1] + ", lng:" + info[2] + ")";
                listModel.addElement(item);
            }
            listPlaces.setModel(listModel);
        } catch (IOException ex) {
            Logger.getLogger(MainFrameClient.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnPlaceListActionPerformed

    private void btnListFriendsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListFriendsActionPerformed
        try {
            String[] friends = this.client.listFriends();
            DefaultListModel listModel = new DefaultListModel();
            for (String friend : friends) {
                listModel.addElement(friend);
            }
            listPlaces.setModel(listModel);
        } catch (IOException ex) {
            Logger.getLogger(MainFrameClient.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnListFriendsActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            String address = txtPlaceName.getText();
            float lat = Float.parseFloat(txtLatitude.getText());
            float lng = Float.parseFloat(txtLongtitude.getText());

            if (this.client.addPlace(address, lat, lng)) {
                JOptionPane.showMessageDialog(this, "Successfully!");
            }
        } catch (IOException | NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "You haven't mark a place in map!");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

