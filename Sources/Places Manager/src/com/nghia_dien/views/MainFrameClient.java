    private void btnConnectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConnectActionPerformed
        String serverIP = txtHost.getText().trim();
        int serverPort;
        try {
            serverPort = Integer.parseInt(txtPort.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please check server ip or server port again!");
            return;
        }

        try {
            this.client = new Client(serverIP, serverPort);
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Connect failed!");
            return;
        }

        String username = txtUsername.getText().trim();
        String password = new String(this.password.getPassword());
        try {
            if (client.login(username, password)) {
                showMap();
                btnConnect.setEnabled(false);
                btnDisconnect.setEnabled(true);
                btnConnectStatus.setIcon(new ImageIcon("src/com/nghia_dien/resources/OK.png"));
                pnlMap.setVisible(true);
                btnSave.setEnabled(true);
                btnListFriends.setEnabled(true);
                btnPlaceList.setEnabled(true);
                btnClearMarkers.setEnabled(true);
                btnTag.setEnabled(true);
            }
        } catch (IOException ex) {
            Logger.getLogger(MainFrameClient.class.getName()).log(Level.SEVERE, null, ex);
            return;
        }


    }//GEN-LAST:event_btnConnectActionPerformed

