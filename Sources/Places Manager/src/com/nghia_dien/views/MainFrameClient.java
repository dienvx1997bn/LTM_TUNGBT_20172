    private void btnTagActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTagActionPerformed
        String placeName;
        float latitude;
        float longtitude;
        try {
            placeName = txtPlaceName.getText();
            latitude = Float.parseFloat(txtLatitude.getText());
            longtitude = Float.parseFloat(txtLongtitude.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showConfirmDialog(this, "Please mark a place before tagging friend!");
            return;
        }
        String username = JOptionPane.showInputDialog(this, "Enter username");
        if (username.length() <= 0) {
            return;
        }
        //   client.addFriend(username);
        try {
            if (this.client.tagFriend(username, placeName, latitude, longtitude) == false) {
                JOptionPane.showMessageDialog(this, "Can't tag your friend in this place.");
            } else {
                JOptionPane.showMessageDialog(this, "Successfully!");
            }

        } catch (IOException ex) {
            Logger.getLogger(MainFrameClient.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnTagActionPerformed

