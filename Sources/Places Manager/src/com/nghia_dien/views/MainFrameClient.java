    public static class JavaCallback {

        public void onMarkerAdded(JSObject position) {
            double lat = position.getProperty("lat").getNumberValue();
            double lng = position.getProperty("lng").getNumberValue();
            String placeName = position.getProperty("address").getStringValue();

            txtLongtitude.setText(String.valueOf(lng));
            txtLatitude.setText(String.valueOf(lat));
            txtPlaceName.setText(placeName);
        }
    }

